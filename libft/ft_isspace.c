int	ft_isspace(int c)
{
	if (c == (int)'\t'
		|| c == (int)'\n'
		|| c == (int)'\v'
		|| c == (int)'\f'
		|| c == (int)'\r'
		|| c == (int)' ')
		return (1);
	return (0);
}
